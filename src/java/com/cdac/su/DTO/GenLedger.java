public class GenLedger {
    private String month;
    private String date;
    private String account;
    private String accDesc;
    private long debit;
    private long credit;
    private String jorHead;
    private long cumDbt;
    private long cumCrt;

    public String getAccDesc() {
        return accDesc;
    }

    public void setAccDesc(String accDesc) {
        this.accDesc = accDesc;
    }

    public String getAccount() {
        return account;
    }

    public void setAccount(String account) {
        this.account = account;
    }

    public long getCredit() {
        return credit;
    }

    public void setCredit(long credit) {
        this.credit = credit;
    }

    public long getCumCrt() {
        return cumCrt;
    }

    public void setCumCrt(long cumCrt) {
        this.cumCrt = cumCrt;
    }

    public long getCumDbt() {
        return cumDbt;
    }

    public void setCumDbt(long cumDbt) {
        this.cumDbt = cumDbt;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

   

    public long getDebit() {
        return debit;
    }

    public void setDebit(long debit) {
        this.debit = debit;
    }

    public String getJorHead() {
        return jorHead;
    }

    public void setJorHead(String jorHead) {
        this.jorHead = jorHead;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }
    
    
   
}
