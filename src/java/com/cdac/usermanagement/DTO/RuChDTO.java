public class RuChDTO {
    private String ruId;
    private String chId;
    
    RuChDTO()
    {
    
    }
    
    RuChDTO(String ruId,String chId)
    {
    this.ruId=ruId;
    this.chId=chId;
    }

    public String getChId() {
        return chId;
    }

    public void setChId(String chId) {
        this.chId = chId;
    }

    public String getRuId() {
        return ruId;
    }

    public void setRuId(String ruId) {
        this.ruId = ruId;
    }
    
    
    
}
