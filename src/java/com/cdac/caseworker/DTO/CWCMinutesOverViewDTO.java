package com.cdac.caseworker.DTO;

import java.util.Date;

/**
 *This class is added by anupam on 8-2-2012
 * to keep data of a cwc meeting overview 
 * it will keep the cwc meeting date and
 * no of profiles visited on the cooesponding day 
 * in a perticular home. 
 * @author ANUPAM
 */
public class CWCMinutesOverViewDTO {
    
    private Date orderDate;
    private long noOfProfilesVisited;

    public CWCMinutesOverViewDTO(Date orderDate, long noOfProfilesVisited) {
        this.orderDate = orderDate;
        this.noOfProfilesVisited = noOfProfilesVisited;
    }
    
    

    public long getNoOfProfilesVisited() {
        return noOfProfilesVisited;
    }

    public void setNoOfProfilesVisited(long noOfProfilesVisited) {
        this.noOfProfilesVisited = noOfProfilesVisited;
    }

    public Date getOrderDate() {
        return orderDate;
    }

    public void setOrderDate(Date orderDate) {
        this.orderDate = orderDate;
    }
    
    
    
}
